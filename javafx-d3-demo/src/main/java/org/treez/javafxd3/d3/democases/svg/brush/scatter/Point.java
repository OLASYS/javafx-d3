package org.treez.javafxd3.d3.democases.svg.brush.scatter;

public class Point {

	public String xTrait;
	public String yTrait;
	public double i;
	public double j;

	public Point(final String xTrait, final String yTrait, final int i, final int j) {
		super();
		this.xTrait = xTrait;
		this.yTrait = yTrait;
		this.i = i;
		this.j = j;
	}
}